public class if_else_if {
 public static void main(String[] args) {
     int num=-30;
     if(num>0)
     {
        System.out.println("Positive");
     }
     else if(num<0)
     {
        System.out.println("Negative");
     }
     else
     {
        System.out.println("Zero");
     }
 }
}
