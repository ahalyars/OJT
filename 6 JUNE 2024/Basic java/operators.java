public class operators {
    public static void main(String[] args) {
        //arithmetic operator
        int x=10;
        int y=2;
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x/y);
        System.out.println(x*y);
System.out.println("******************");


        //Relational Operator
        int a=10;
        int b=20;
         System.out.println(a>b);
         System.out.println(a<b);
         System.out.println(a==b);
         System.out.println(a!=b);
System.out.println("******************");


        //Logical Operator
        boolean ab=true;
        boolean bc=false;
        System.out.println(ab && bc);
        System.out.println(ab && ab);
        System.out.println(ab || bc);
        System.out.println (!(ab && bc));


    }
}
